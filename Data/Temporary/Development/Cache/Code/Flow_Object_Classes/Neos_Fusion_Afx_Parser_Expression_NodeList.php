class NodeList_Original
{
    /**
     * @param Lexer $lexer
     * @return array
     * @throws AfxParserException
     */
    public static function parse(Lexer $lexer): array
    {
        $contents = [];
        $currentText = '';
        while (!$lexer->isEnd()) {
            if ($lexer->isOpeningBracket()) {
                $lexer->consume();
                if ($currentText !== '') {
                    $contents[] = [
                        'type' => 'text',
                        'payload' => $currentText
                    ];
                }
                if ($lexer->isForwardSlash()) {
                    $lexer->rewind();
                    return $contents;
                }
                if ($lexer->isExclamationMark()) {
                    $lexer->rewind();
                    $contents[] = [
                        'type' => 'comment',
                        'payload' => Comment::parse($lexer)
                    ];
                    $currentText = '';
                    continue;
                } else {
                    $lexer->rewind();
                    $contents[] = [
                        'type' => 'node',
                        'payload' => Node::parse($lexer)
                    ];
                    $currentText = '';
                    continue;
                }
            }

            if ($lexer->isOpeningBrace()) {
                if ($currentText) {
                    $contents[] = [
                        'type' => 'text',
                        'payload' => $currentText
                    ];
                }

                $contents[] = [
                    'type' => 'expression',
                    'payload' => Expression::parse($lexer)
                ];
                $currentText = '';
                continue;
            }

            $currentText .= $lexer->consume();
        }

        if ($lexer->isEnd() && $currentText !== '') {
            $contents[] = [
                'type' => 'text',
                'payload' => $currentText
            ];
        }

        return $contents;
    }
}

